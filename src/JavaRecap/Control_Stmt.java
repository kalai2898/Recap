package JavaRecap;

public class Control_Stmt {

	public static void main(String[] args)
	{
		//For loop and if condition
		for(int i=0;i<=10;i++)
		{
			if(i==5)
			{
				System.out.println(i);
			}
	     }
		System.out.println("-----------------------------------");
		//while Loop
		    int j=0;
		    while(j<5)
			{
				System.out.println(j);	
				j++;
			}
	    System.out.println("-----------------------------------");  
	    //do-While Loop
	    int l=0;
	    do
	    {
	    	System.out.println(l);
	    	l++;
	    	
	    }while(l<10);
		    
			
			
		}
}

	


